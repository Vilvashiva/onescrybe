(function () {
  // Modified to run on homepage (root path)
  var path = location.pathname.replace(/\/+$/, '') + '/';
  if (path !== '/') return; // Only run on homepage

  var KEY = 'os_pricing_modal_dismissed_until';
  var now = Date.now();
  var until = parseInt(localStorage.getItem(KEY) || '0', 10);
  if (until && now < until) return; // still within cool-down window

  var modal = document.getElementById('os-pricing-modal');
  if (!modal) return;
  var closeEls = modal.querySelectorAll('[data-close]');
  var dontShow = document.getElementById('os-dont-show');
  var dialog = modal.querySelector('.os-modal__dialog');

  // Focus management
  var focusableSel = 'a,button,input,textarea,select,[tabindex]:not([tabindex="-1"])';
  var lastFocused = document.activeElement;

  function trapFocus(e) {
    var focusables = dialog.querySelectorAll(focusableSel);
    if (!focusables.length) return;
    var first = focusables[0], last = focusables[focusables.length - 1];
    if (e.key === 'Tab') {
      if (e.shiftKey && document.activeElement === first) { e.preventDefault(); last.focus(); }
      else if (!e.shiftKey && document.activeElement === last) { e.preventDefault(); first.focus(); }
    }
    if (e.key === 'Escape') close();
  }

  function open() {
    modal.setAttribute('aria-hidden', 'false');
    document.body.classList.add('os-noscroll');
    // focus first actionable
    setTimeout(function(){
      var f = dialog.querySelector(focusableSel);
      if (f) f.focus();
    }, 0);
    document.addEventListener('keydown', trapFocus);
  }

  function close() {
    if (dontShow && dontShow.checked) {
      var days14 = 14 * 24 * 60 * 60 * 1000;
      localStorage.setItem(KEY, String(Date.now() + days14));
    }
    modal.setAttribute('aria-hidden', 'true');
    document.body.classList.remove('os-noscroll');
    document.removeEventListener('keydown', trapFocus);
    if (lastFocused && lastFocused.focus) lastFocused.focus();
  }

  closeEls.forEach(function (el) { el.addEventListener('click', close); });
  // Close on backdrop click
  modal.addEventListener('click', function (e) { if (e.target.dataset.close) close(); });

  // Open on DOM ready
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', open);
  } else {
    open();
  }
})();
